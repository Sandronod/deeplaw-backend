    private function haystack(string $question, ?TriageResult $triage, ?IssueList $issueList): string
    {
        $parts = [$question, (string) ($triage?->searchQuery ?? '')];

        foreach ($issueList?->issues ?? [] as $issue) {
            $parts[] = $issue->title;
            $parts[] = implode(' ', $issue->keywords);
            $parts[] = $issue->domain;
        }

        return mb_strtolower(implode("\n", array_filter($parts)));
    }

    /**
     * @param array<int, string> $domains
     * @param array<int, string> $needles
     */
    private function hasDomain(array $domains, array $needles): bool
    {
        return (bool) array_intersect($domains, $needles);
    }

    /**
     * @param array<int, string> $needles
     */
    private function containsAny(string $text, array $needles): bool
    {
        foreach ($needles as $needle) {
            if (str_contains($text, mb_strtolower($needle))) {
                return true;
            }
        }

        return false;
    }

    /**
     * @param array<int, array{key:string, label:string, lines:array<int,string>}> $items
     * @return array<int, array{key:string, label:string, lines:array<int,string>}>
     */
    private function uniqueByKey(array $items): array
    {
        $seen = [];
        $unique = [];

        foreach ($items as $item) {
            if (isset($seen[$item['key']])) {
                continue;
            }

            $seen[$item['key']] = true;
            $unique[] = $item;
        }

        return $unique;
    }
